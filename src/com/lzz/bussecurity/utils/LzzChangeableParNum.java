package com.lzz.bussecurity.utils;

public class LzzChangeableParNum {
	private int num;
	
	public LzzChangeableParNum(int num){
		this.num = num;
	}
	
	public int getNum(){
		return num;
	}
	
	public int add(int i){
		num += i;
		return num;
	}
	
	public int add(){
		num ++;
		return num;
	}
	
	public int dec(int i){
		num -= i;
		return num;
	}
	
	public int dec(){
		num --;
		return num;
	}
}
